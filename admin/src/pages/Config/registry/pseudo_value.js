export function getPseudo(obj, serviceCode) {
  allKeys[obj.fieldName] = obj.fieldLabel;
  if (obj.fieldName == 'licenseType') {
    const values = ['Trading', 'Manufacturing'];
    return values[Math.floor(Math.random() * values.length)];
  } else if (obj.fieldName.toLowerCase().includes('origin')) {
    return 'South Africa';
  } else if (obj.fieldName.toLowerCase().includes('addressline1')) {
    return 'Plot 9709';
  } else if (obj.fieldName.toLowerCase().includes('addressline2')) {
    return 'Sesame Street';
  } else if (obj.fieldName.toLowerCase().includes('country')) {
    return 'Botswana';
  } else if (obj.fieldName.toLowerCase().includes('tariffcode')) {
    return '8374637';
  } else if (obj.fieldName.toLowerCase().includes('entryport')) {
    return 'Kopfontein';
  } else if (obj.fieldName.toLowerCase().includes('locality')) {
    return 'Gaborone';
  } else if (obj.fieldName.includes('Att')) {
    let out = {
      bucket: 'MTI_007_12_014',
      extension: 'pdf',
      'original-name': 'certificates_BW000001FR.pdf',
      key: '78b5faab-6992-40af-9775-5688e75b7845'
    };

    if (defaults[serviceCode]) {
      out.bucket = serviceCode;
      out.key = defaults[serviceCode];
    }
    return out;
  } else if (obj.fieldName.toLowerCase().includes('declaration')) {
    return true;
  } else if (['trailerregno', 'vehicleregno'].includes(obj.fieldName.toLowerCase())) {
    return 'B123ABC';
  } else if (obj.fieldName.toLowerCase().includes('licenseno')) {
    return 'BW00000001234';
  } else if (obj.fieldName.toLowerCase().includes('destination')) {
    return 'Johanesburg';
  } else if (['contactnumber', 'telephone', 'phone'].includes(obj.fieldName.toLowerCase())) {
    return '72482827';
  } else if (obj.fieldName.toLowerCase().includes('email')) {
    return 'info@devsql.co.bw';
  } else if (obj.fieldLabel.toLowerCase().includes('quantity')) {
    return '3';
  } else if (
    obj.fieldLabel.toLowerCase().includes('bwp') ||
    obj.fieldName.toLowerCase().includes('value')
  ) {
    return 'P500.00';
  } else if (['companyname', 'businessname'].includes(obj.fieldName.toLowerCase())) {
    return 'DevSQL Pty Ltd';
  } else if (['tinNo', 'companyTinNo'].includes(obj.fieldName)) {
    return 'BW00003997901';
  } else if (['applicantIdNo'].includes(obj.fieldName)) {
    return '720914114';
  } else if (['companyRegNo', 'businessRegNo'].includes(obj.fieldName)) {
    return 'BW00003997901';
  } else if (obj.fieldName.toLowerCase().includes('date') || obj.fieldName.fieldType == 'Date') {
    return '2022-06-20';
  } else if (['deceasedIdNo'].includes(obj.fieldName)) {
    return '376021215';
  } else {
    allKeys[obj.fieldName] = `#######${obj.fieldLabel}`;
    someKeys[obj.fieldName] = `#######${obj.fieldLabel}`;

    return defaults[obj.fieldName] || `#${obj.fieldLabel}`;
  }
}

